import turtle
turtle.Screen().bgcolor("green")
turtle.Screen().setup(300,400)
star=turtle.Turtle()
angle=360/3
star.forward(100)
star.left(angle)
star.forward(100)
star.left(angle)
star.forward(100)
star.penup()
star.forward(25)
star.right(angle)
star.forward(75)
star.pendown()
star.forward(100)
star.right(angle)
star.forward(100)
star.right(angle)
star.forward(100)
turtle.done()